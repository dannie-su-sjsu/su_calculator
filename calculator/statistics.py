# calculator/statistics.py
import statistics

# mean
def mean(x):
  return statistics.mean(x)

# median
def median(x):
  return statistics.median(x)

# mode
def mode(x):
  return statistics.mode(x)

# standard deviation
def std_dev(x):
  return statistics.stdev(x)

